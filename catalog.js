// Настройка кнопок фильтров
function setupFilterButtons() {
    const filterButtons = document.querySelectorAll('.filter-btn');
    
    if (!filterButtons.length) {
        console.error('Кнопки фильтров не найдены');
        return;
    }
    
    console.log(`Найдено ${filterButtons.length} кнопок фильтров`);
    
    filterButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            
            // Удаляем активный класс у всех кнопок
            filterButtons.forEach(btn => btn.classList.remove('active'));
            
            // Добавляем активный класс к нажатой кнопке
            this.classList.add('active');
            
            // Получаем фильтр
            const filterKey = this.getAttribute('data-filter');
            currentFilter = filterKey;
            
            console.log(`Выбран фильтр: ${filterKey}`);
            
            // Фильтруем продукты
            currentProducts = filters[filterKey].products;
            
            // Рендерим продукты
            renderProducts(currentProducts);
            updateProductsCount();
            
            console.log(`Отфильтровано ${currentProducts.length} товаров`);
        });
    });
}

