// Рендеринг товаров
function renderProducts(products) {
    const productsGrid = document.getElementById('productsGrid');
    
    if (!productsGrid) {
        console.error('Контейнер сетки продуктов не найден');
        return;
    }
    
    if (products.length === 0) {
        productsGrid.innerHTML = `
            <div class="no-products">
                <p>В данной категории пока нет товаров</p>
            </div>
        `;
        return;
    }
    
    console.log(`Рендеринг ${products.length} товаров...`);
    
    const productsHTML = products.map(product => createProductCard(product)).join('');
    productsGrid.innerHTML = productsHTML;
    
    // Добавляем обработчики для кнопок "Подробнее"
    const detailButtons = productsGrid.querySelectorAll('.product-detail-btn');
    detailButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.stopPropagation();
            const productId = parseInt(this.getAttribute('data-product-id'));
            const product = products.find(p => p.id === productId);
            if (product) {
                console.log(`Открытие модального окна для товара: ${product.name}`);
                openProductModal(product);
            }
        });
    });
    
    console.log('Рендеринг товаров завершен');
}

// Создание карточки товара
function createProductCard(product) {
    const badgeHTML = product.badge ? 
        `<div class="product-badge ${product.badge}">${product.badge === 'new' ? 'Новинка' : 'Хит продаж'}</div>` : '';
    
    return `
        <div class="catalog-product-card" data-product-id="${product.id}">
            ${badgeHTML}
            <div class="catalog-product-image">
                <img src="${product.image}" alt="${product.name}" class="catalog-product-img">
            </div>
            <div class="catalog-product-info">
                <h3 class="catalog-product-title">${product.name}</h3>
                <button class="product-detail-btn" data-product-id="${product.id}">
                    Подробнее
                    <svg width="16" height="16" viewBox="0 0 24 24" fill="none">
                        <path d="M5 12H19M19 12L12 5M19 12L12 19" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"/>
                    </svg>
                </button>
            </div>
        </div>
    `;
}

// Обновление счетчика товаров
function updateProductsCount() {
    const countElement = document.getElementById('productsCount');
    if (countElement) {
        countElement.textContent = currentProducts.length;
    }
}

