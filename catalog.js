// Инициализация модального окна товара
function initProductModal() {
    const modal = document.getElementById('productModal');
    const modalOverlay = document.getElementById('productModalOverlay');
    const modalClose = document.getElementById('productModalClose');
    
    if (!modal || !modalOverlay || !modalClose) {
        console.log('Элементы модального окна товара не найдены');
        return;
    }
    
    console.log('Инициализация модального окна товара...');
    
    // Закрытие модального окна
    modalClose.addEventListener('click', closeProductModal);
    modalOverlay.addEventListener('click', closeProductModal);
    
    // Закрытие по Escape
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && modal.classList.contains('active')) {
            closeProductModal();
        }
    });
    
    console.log('Модальное окно товара инициализировано');
}

// Открытие модального окна товара
function openProductModal(product) {
    const modal = document.getElementById('productModal');
    const modalImage = document.getElementById('modalProductImage');
    const modalTitle = document.getElementById('modalProductTitle');
    const modalDescription = document.getElementById('modalProductDescription');
    const modalBadge = document.getElementById('modalProductBadge');
    
    if (!modal || !modalImage || !modalTitle || !modalDescription) {
        console.error('Элементы модального окна не найдены');
        return;
    }
    
    console.log(`Открытие модального окна для товара ID: ${product.id}`);
    
    // Заполняем содержимое модального окна
    modalImage.src = product.image;
    modalImage.alt = product.name;
    modalTitle.textContent = product.name;
    modalDescription.innerHTML = product.description;
    
    // Показываем или скрываем бейдж
    if (product.badge && modalBadge) {
        modalBadge.textContent = product.badge === 'new' ? 'Новинка' : 'Хит продаж';
        modalBadge.className = `product-modal-badge ${product.badge}`;
        modalBadge.style.display = 'block';
    } else if (modalBadge) {
        modalBadge.style.display = 'none';
    }
    
    // Показываем модальное окно
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
    
    console.log('Модальное окно открыто');
}

// Закрытие модального окна товара
function closeProductModal() {
    const modal = document.getElementById('productModal');
    if (modal) {
        modal.classList.remove('active');
        document.body.style.overflow = '';
        console.log('Модальное окно закрыто');
    }
}

